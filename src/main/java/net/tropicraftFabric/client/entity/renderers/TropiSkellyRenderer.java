package net.tropicraftFabric.client.entity.renderers;

import net.tropicraftFabric.Constants;
import net.tropicraftFabric.client.entity.models.TropiSkellyModel;
import net.tropicraftFabric.common.entity.hostile.TropiSkellyEntity;
import net.minecraft.client.render.entity.BipedEntityRenderer;
import net.minecraft.client.render.entity.EntityRenderDispatcher;
import net.minecraft.client.render.entity.feature.ElytraFeatureRenderer;
import net.minecraft.client.render.entity.feature.HeadFeatureRenderer;
import net.minecraft.client.render.entity.feature.HeldItemFeatureRenderer;
import net.minecraft.util.Identifier;

public class TropiSkellyRenderer extends BipedEntityRenderer<TropiSkellyEntity, TropiSkellyModel> {

    private static final Identifier TEXTURE = new Identifier(Constants.MODID, "textures/entity/tropiskeleton.png");

    public TropiSkellyRenderer(EntityRenderDispatcher rendererManager) {
        super(rendererManager, new TropiSkellyModel(), 0.5F);

        features.clear();

        addFeature(new HeadFeatureRenderer<>(this));
        addFeature(new ElytraFeatureRenderer<>(this));
        addFeature(new HeldItemFeatureRenderer<>(this));
    }

    @Override
    public Identifier getTexture(TropiSkellyEntity entity) {
        return TEXTURE;
    }
}
